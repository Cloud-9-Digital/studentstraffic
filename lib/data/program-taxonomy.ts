export type CanonicalProgrammeSlug = (typeof canonicalProgrammes)[number]["slug"];

const canonicalProgrammeBySlug = new Map(
  canonicalProgrammes.map((programme) => [programme.slug, programme]),
);

export function getCanonicalProgramme(slug: string) {
  return canonicalProgrammeBySlug.get(slug as CanonicalProgrammeSlug) ?? null;
}

export function isApprovedCanonicalProgramme(slug: string): slug is CanonicalProgrammeSlug {
  return canonicalProgrammeBySlug.has(slug as CanonicalProgrammeSlug);
}
